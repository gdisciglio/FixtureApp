    private void btnGAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGAActionPerformed
        // TODO add your handling code here:
        
        tab = 0;
        if (!Tabla.abierta) 
        {  
            //creo una ventana interna 
            Tabla v = new Tabla();
            //agrego la ventana interna al contenedor de ventanas internas (jDescktopPane)
            jDesktopPane.add(v);
      
            //hago visible la ventana tabla, centrada
            Dimension desktopSize = jDesktopPane.getSize();
            Dimension FrameSize = v.getSize();
            v.setLocation((desktopSize.width - FrameSize.width) / 2, (desktopSize.height - FrameSize.height) / 2);
            v.show();
            //v.setVisible(true);
           
            setDefaultCloseOperation(EXIT_ON_CLOSE);
        }
        
      
    }//GEN-LAST:event_btnGAActionPerformed

